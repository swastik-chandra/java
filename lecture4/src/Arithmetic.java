public class Arithmetic {
    public static void main(String[] args) {
        int  a = 8;
        int b = 5;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        double X = 4;
        double Y = 3;
        System.out.println(X + Y);
        System.out.println(X - Y);
        System.out.println(X * Y);
        System.out.println(X % Y);

    }

}
